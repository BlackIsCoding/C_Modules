#include <unistd.h>

int	ft_iterative_power(int nb, int power)
{
	int	res;

	if (power < 0)
	{
		return (0);
	}
	else if (nb == 0 && power == 0)
	{
		return (1);
	}
	else
	{
		res = 1;
		while (power > 0)
		{
			res = res * nb;
			power--;
		}
		return (res);
	}
}
