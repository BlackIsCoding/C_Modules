#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	moitie;

	moitie = nb / 2;
	i = 0;
	while (i <= moitie)
	{
		if (i * i == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
